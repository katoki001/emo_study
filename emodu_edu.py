v, v0, a, t, d, m, F, g = symbols('v v0 a t d m F g')
KE, PE, W, P, r, freq, lam, c = symbols('KE PE W P r f lam c')
V, I, R, Q, mc, dT = symbols('V I R Q mc dT')

FORMULAS = {
    "vf":   Eq(v,    v0 + a * t),
    "dist": Eq(d,    v0 * t + Float('0.5') * a * t ** 2),
    "vf2":  Eq(v**2, v0**2 + 2 * a * d),
    "fall": Eq(d,    Float('0.5') * g * t ** 2),
    "F=ma": Eq(F,    m * a),
    "KE":   Eq(KE,   Float('0.5') * m * v ** 2),
    "PE":   Eq(PE,   m * g * d),
    "W":    Eq(W,    F * d),
    "P":    Eq(P,    W / t),
    "wave": Eq(c,    freq * lam),
    "ohm":  Eq(V,    I * R),
    "heat": Eq(Q,    mc * dT),
}

EXTRACT_PATTERNS = {
    "v0":   r"(?:initial(?:ly)?|starts? at|traveling at|moving at)\s+([\d.]+)\s*m/?s",
    "v":    r"(?:final(?:ly)?|reaches?|accelerates? to|slows? to)\s+([\d.]+)\s*m/?s",
    "t":    r"(?:in|takes?|after|for)\s+([\d.]+)\s*(?:s|sec|second)",
    "d":    r"(?:distance|height|cliff|travels?)\s+(?:of\s+)?([\d.]+)\s*m",
    "m":    r"([\d.]+)\s*kg",
    "F":    r"([\d.]+)\s*N\b",
    "a":    r"accelerat\w+\s+(?:at\s+)?([\d.]+)\s*m/?s",
    "freq": r"([\d.]+)\s*Hz",
    "lam":  r"wavelength\s+(?:of\s+)?([\d.]+)",
    "V":    r"([\d.]+)\s*[Vv]olts?",
    "I":    r"([\d.]+)\s*[Aa]mps?",
    "R":    r"([\d.]+)\s*ohms?",
    "mc":   r"specific heat\s+([\d.]+)",
    "dT":   r"temperature change\s+([\d.]+)",
    # ── NEW: generic speed pattern for "constant speed of X m/s" ──
    "speed": r"(?:constant\s+)?speed\s+of\s+([\d.]+)\s*m/?s",
}

SYM_MAP = {
    "v": v, "v0": v0, "a": a, "t": t, "d": d, "m": m, "F": F, "g": g,
    "KE": KE, "PE": PE, "W": W, "P": P, "freq": freq, "lam": lam,
    "c": c, "V": V, "I": I, "R": R, "Q": Q, "mc": mc, "dT": dT,
}


def extract(question):
    vals = {}
    for var, pat in EXTRACT_PATTERNS.items():
        match = re.search(pat, question, re.IGNORECASE)
        if match:
            vals[var] = float(match.group(1))

    # ── FIX: handle "constant speed" → treat as v0=v=speed, a=0 ──
    if "speed" in vals and "v0" not in vals and "v" not in vals:
        vals["v0"] = vals["speed"]
        vals["v"]  = vals["speed"]
        vals["a"]  = 0.0

    return vals


def route(question, vals):
    q   = question.lower()
    has = vals.__contains__

    # ── FIX: constant speed + time → distance (d = v*t) ──
    if any(w in q for w in ["how far", "distance", "travel"]):
        if has("v0") and has("a") and vals.get("a") == 0.0 and has("t"):
            return "dist", d

    if any(w in q for w in ["dropped", "falls", "cliff", "how high"]):
        vals.setdefault("v0", 0.0)
        vals.setdefault("g", 9.8)
        if has("t"): return "fall", d
        if has("d"): return "fall", t

    if "acceleration" in q:
        if has("v0") and has("v") and has("t"): return "vf",   a
        if has("v0") and has("v") and has("d"): return "vf2",  a
        if has("m")  and has("F"):              return "F=ma", a

    if any(w in q for w in ["distance", "how far", "travel"]):
        if has("v0") and has("a") and has("t"): return "dist", d
        if has("v0") and has("v") and has("d"): return "vf2",  d

    if any(w in q for w in ["final velocity", "final speed", "how fast"]):
        if has("v0") and has("a") and has("t"): return "vf",  v
        if has("v0") and has("a") and has("d"): return "vf2", v

    if "force"            in q and has("m") and has("a"):            return "F=ma", F
    if "kinetic energy"   in q and has("m") and has("v"):            return "KE",   KE
    if "potential energy" in q and has("m") and has("d"):
        vals.setdefault("g", 9.8);                                    return "PE",   PE
    if "work"  in q and has("F") and has("d"):                       return "W",    W
    if "power" in q and has("W") and has("t"):                       return "P",    P
    if "wave"  in q and has("freq") and has("lam"):                  return "wave", c
    if any(w in q for w in ["ohm", "voltage", "current"]):
        if has("I") and has("R"): return "ohm", V
        if has("V") and has("R"): return "ohm", I
        if has("V") and has("I"): return "ohm", R
    if "heat" in q and has("mc") and has("dT"):                      return "heat", Q

    return None, None


def solve_physics(question):
    vals     = extract(question)
    key, unk = route(question, vals)
    if key is None:
        return None
    subs = {SYM_MAP[k]: val for k, val in vals.items() if k in SYM_MAP}
    try:
        res  = solve(FORMULAS[key].subs(subs), unk)
        nums = [float(x) for x in res if x.is_real]
        pos  = [x for x in nums if x >= 0]
        ans  = pos[0] if pos else (nums[0] if nums else None)
        if ans is None:
            return None
        return {"answer": ans, "formula": str(FORMULAS[key]), "vals": vals}
    except Exception:
        return None

