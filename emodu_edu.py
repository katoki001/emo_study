"""Summarization block"""

def read_document(file_path: str) -> str:
    ext = file_path.lower().split(".")[-1]
    if ext == "pdf":
        doc  = fitz.open(file_path)
        text = "".join(page.get_text() for page in doc)
        doc.close()
        return text.strip()
    elif ext == "docx":
        doc = DocxDocument(file_path)
        return "\n".join(p.text for p in doc.paragraphs if p.text.strip())
    elif ext in ("txt", "md"):
        with open(file_path, "r", encoding="utf-8", errors="ignore") as f:
            return f.read()
    else:
        raise ValueError(f"Unsupported file type: .{ext}")


def summarize_document(file_path: str) -> str:
    text     = read_document(file_path)
    response = groq_client.chat.completions.create(
        model="llama-3.1-8b-instant",
        messages=[{
            "role": "user",
            "content": (
                "Summarize the following text clearly and accurately. "
                "Only use information from the text.\n\n" + text
            ),
        }],
        max_tokens=1024,
    )
    return response.choices[0].message.content

"""Flashcards"""

def generate_flashcards_from_text(text: str, num_cards: int = 5) -> list:
    system_prompt = (
        "You are a flashcard generator. "
        "Given a text, produce EXACTLY a JSON array of flashcard objects. "
        'Each object must have exactly two keys: "question" and "answer". '
        "The question must be a clear, standalone question. "
        "The answer must be concise and direct. "
        "NEVER repeat or include the question text inside the answer field. "
        "Output ONLY the raw JSON array, no explanation, no markdown, no code fences."
    )
    user_prompt = (
        f"Create {num_cards} flashcards from this text:\n\n{text[:2000]}\n\n"
        'Respond with ONLY a JSON array like:\n'
        '[{"question":"What is X?","answer":"X is ..."}]\n'
        "Do NOT put the question inside the answer."
    )
    try:
        response = groq_client.chat.completions.create(
            model="llama-3.1-8b-instant",
            messages=[
                {"role": "system", "content": system_prompt},
                {"role": "user",   "content": user_prompt},
            ],
            max_tokens=1024,
            temperature=0.3,
        )
        raw = response.choices[0].message.content.strip()
        raw = re.sub(r"```[a-z]*\n?", "", raw).strip()
        start = raw.index("[")
        end   = raw.rindex("]") + 1
        cards = json.loads(raw[start:end])
        clean = []
        for c in cards:
            q = str(c.get("question", "")).strip()
            a = str(c.get("answer",   "")).strip()
            if not q or not a:
                continue
            if a.lower().startswith(q.lower()):
                a = a[len(q):].lstrip(" :.–-")
            clean.append({"question": q, "answer": a})
        return clean if clean else _fallback_cards(text, num_cards)
    except Exception as e:
        print(f"Flashcard Groq error: {e}")
        return _fallback_cards(text, num_cards)


def _fallback_cards(text: str, num_cards: int) -> list:
    sentences = [s.strip() for s in re.split(r"(?<=[.!?])\s+", text) if len(s.strip()) > 20]
    cards = []
    for i in range(0, min(len(sentences) - 1, num_cards * 2), 2):
        cards.append({
            "question": sentences[i],
            "answer":   sentences[i + 1] if i + 1 < len(sentences) else "See summary.",
        })
        if len(cards) >= num_cards:
            break
    return cards or [{"question": "What is this lecture about?", "answer": text[:300]}]

