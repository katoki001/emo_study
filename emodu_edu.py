def translate_to_armenian(text: str) -> str:
    """Translate English → Armenian using Helsinki model."""
    if not text or not text.strip():
        return text
    translated_lines = []
    for line in text.split('\n'):
        if not line.strip():
            translated_lines.append(line)
            continue
        inputs = _en_hy_tokenizer(
            [line],
            return_tensors="pt",
            truncation=True,
            max_length=512,
            padding=True,
        ).to(device)
        with torch.no_grad():
            outputs = _en_hy_model.generate(**inputs, num_beams=4, early_stopping=True)
        translated = _en_hy_tokenizer.decode(outputs[0], skip_special_tokens=True)
        translated = clean_armenian(translated)
        translated_lines.append(translated)
    return '\n'.join(translated_lines)


