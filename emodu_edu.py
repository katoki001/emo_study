@lru_cache(maxsize=512)
def retrieve_cached(query: str, k: int = 5):
    q_emb      = embed_model.encode([query], normalize_embeddings=True).astype("float32")
    _, I       = index.search(q_emb, k * 4)
    candidates = [(texts[i], i) for i in I[0]]
    detected   = detect_topic(query)

    if detected and "topic" in texts_df.columns:
        matched   = [t for t, i in candidates if texts_df.iloc[i]["topic"] == detected]
        unmatched = [t for t, i in candidates if texts_df.iloc[i]["topic"] != detected]
        ranked    = (matched + unmatched)[:k]
    else:
        ranked = [t for t, _ in candidates[:k]]
    return tuple(ranked)


def retrieve(query, k=5):
    chunks     = retrieve_cached(query, k)
    chunks     = [c for c in chunks if c and len(c.strip()) > 20]
    links      = [c for c in chunks if " — http" in c]
    texts_only = [c for c in chunks if " — http" not in c]
    return texts_only, links

