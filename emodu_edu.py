def fc_generate(prompt: str, max_new_tokens: int = 400) -> str:
    """flan-t5-base — flashcard JSON generation."""
    inputs = fc_tokenizer(
        prompt, return_tensors="pt", truncation=True, max_length=512
    ).to(device)
    with torch.no_grad():
        outputs = fc_model.generate(
            **inputs,
            max_new_tokens=max_new_tokens,
            num_beams=2,
            no_repeat_ngram_size=3,
            early_stopping=True,
            pad_token_id=fc_tokenizer.pad_token_id,
        )
    return fc_tokenizer.decode(outputs[0], skip_special_tokens=True).strip()

